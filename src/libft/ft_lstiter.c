#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*nxt;

	if (lst != NULL)
	{
		nxt = lst;
		while (1)
		{
			(*f)(nxt->content);
			nxt = nxt->nxt;
			if (nxt == NULL)
				return ;
		}
	}
}
